// deterministic "index of the day" so a dua/selection is stable per calendar day
export function dayIndex(len: number, offset = 0): number {
  const now = new Date();
  const dayNum = Math.floor(Date.UTC(now.getFullYear(), now.getMonth(), now.getDate()) / 86400000);
  return ((dayNum + offset) % len + len) % len;
}
